static void ui_font_draw_glyph(ui_font_plot_fn plot,
                               ui_font_rect_fn rect,
                               void *user,
                               int x, int y,
                               const ui_font_glyph_t *g,
                               const uint8_t *bits,
                               uint16_t fg,
                               uint16_t bg)
{
    if (!g || g->w == 0 || g->h == 0)
        return;
    const int bytes_per_row = (g->w + 7) / 8;
    const uint8_t *data = &bits[g->offset];
    int gx = x + g->xoff;
    int gy = y + g->yoff;
    /* Draw background rect if rect callback provided */
    if (rect && bg != 0)
        rect(gx, gy, g->w, g->h, bg, user);
    /* Draw foreground pixels */
    for (int row = 0; row < g->h; ++row) {
        for (int col = 0; col < g->w; ++col) {
            int byte_idx = row * bytes_per_row + (col / 8);
            int bit_idx = 7 - (col % 8);
            if (data[byte_idx] & (1 << bit_idx))
                plot(gx + col, gy + row, fg, user);
        }
    }
}

void ui_font_draw_text(ui_font_plot_fn plot,
                       ui_font_rect_fn rect,
                       void *user,
                       int x, int y,
                       const char *text,
                       ui_font_size_t size,
                       uint16_t fg,
                       uint16_t bg)
{
    if (!text || !plot)
        return;
    const ui_font_data_t *fd = ui_font_get(size);
    int cursor_x = x;
    for (const char *p = text; *p; ++p) {
        const ui_font_glyph_t *g = ui_font_glyph(size, *p);
        ui_font_draw_glyph(plot, rect, user, cursor_x, y, g, fd->bits, fg, bg);
        cursor_x += g->xadv;
    }
}

void ui_font_bitmap_draw_text(ui_font_plot_fn plot,
                              ui_font_rect_fn rect,
                              void *user,
                              int x, int y,
                              const char *text,
                              uint16_t fg,
                              uint16_t bg)
{
    ui_font_draw_text(plot, rect, user, x, y, text, UI_FONT_BODY, fg, bg);
}
